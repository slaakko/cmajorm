std::string KeyText(Keys key)
{
    std::string s;
    if ((key & Keys::controlModifier) != Keys::none)
    {
        s.append("Ctrl+");
    }
    if ((key & Keys::altModifier) != Keys::none)
    {
        s.append("Alt+");
    }
    if ((key & Keys::shiftModifier) != Keys::none)
    {
        s.append("Shift+");
    }
    Keys keyCode = key & ~Keys::modifiers;
    s.append(keyCodeStr[int(keyCode)]);
    return s;
}

